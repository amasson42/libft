#include "libft.h"

void	*ft_memalloc(size_t size)
{
	void	*p;
	size_t	i;

	p = malloc(size);
	if (p)
	{
		i = 0;
		while (i < size)
		{
			*((char *)p + i) = 0;
			i++;
		}
	}
	return (p);
}
